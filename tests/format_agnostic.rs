// Proves the format-agnostic core: markers are ordinary comments in the
// target file's own format. The same fragment syncs into HTML, CSS/JS,
// YAML/shell line comments, Markdown, and config-defined formats — each
// target staying valid in its native syntax.

use std::fs;
use std::path::Path;

fn setup(dir: &Path, fragments: &[(&str, &str)], targets: &[(&str, &str)]) {
    let frag_dir = dir.join("_fragments");
    fs::create_dir_all(&frag_dir).unwrap();
    for (name, content) in fragments {
        fs::write(frag_dir.join(name), content).unwrap();
    }
    for (name, content) in targets {
        let path = dir.join(name);
        if let Some(parent) = path.parent() {
            fs::create_dir_all(parent).unwrap();
        }
        fs::write(path, content).unwrap();
    }
}

fn run(dir: &Path, cmd: &str) -> std::process::Output {
    std::process::Command::new(env!("CARGO_BIN_EXE_fragments"))
        .arg(dir.to_str().unwrap())
        .arg(cmd)
        .output()
        .expect("failed to run fragments")
}

fn read(dir: &Path, name: &str) -> String {
    fs::read_to_string(dir.join(name)).unwrap()
}

#[test]
fn syncs_into_css_block_comments() {
    let tmp = tempfile::TempDir::new().unwrap();
    let root = tmp.path();
    setup(
        root,
        &[("banner.css", ".banner { color: rebeccapurple; }")],
        &[(
            "style.css",
            "body { margin: 0; }\n/* fragment:banner */\n.banner { color: red; }\n/* /fragment:banner */\n",
        )],
    );

    assert!(run(root, "sync").status.success());
    let out = read(root, "style.css");
    assert!(out.contains(".banner { color: rebeccapurple; }"), "{out}");
    assert!(!out.contains("color: red"), "{out}");
    // Markers preserved, file still valid CSS.
    assert!(out.contains("/* fragment:banner */"));
    assert!(out.contains("/* /fragment:banner */"));
}

